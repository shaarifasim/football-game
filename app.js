var mainDiv = document.getElementById("matchList");
for (var key in worldCup) {
  if (typeof worldCup[key] === "object") {
    for (var key2 in worldCup[key].groups) {
      var Box = document.createElement("div");
      Box.setAttribute("class", "group-box");
      Box.setAttribute("onClick", `details(${key2})`);
      Box.appendChild(document.createTextNode(worldCup[key].groups[key2].name));
      mainDiv.appendChild(Box);
    }
  }
}
